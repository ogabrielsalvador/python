
# sem utilizar nenhum tipo primitivo
n1 = input('Digite um valor: ')
print(type(n1))

# utilizando o tipo primitivo int
n2 = int(input('Digite um valor: '))
print(type(n2))

# utilizando o tipo primitivo float
n3 = float(input('Digite um valor: '))
print(type(n3))

# utilizando o tipo primitivo bool
n4 = bool(input('Digite um valor: '))
print(type(n4))

# utilizando o tipo primitivo str
n5 = str(input('Digite um valor: '))
print(type(n5))
